void panel_init_set_sequence(struct dsi_device *dsi)
{
	int  i;

	edo_e1392am1_nop(dsi);
	mdelay(120);

	for(i = 0; i < ARRAY_SIZE(edo_e1392am1_cmd_list); i++) {
		write_command(dsi, edo_e1392am1_cmd_list[i]);
	}
	mdelay(120);

	edo_e1392am1_display_on(dsi);
	debug("init EDO 1.4 400*400 LCD...\n");

	return;
}

