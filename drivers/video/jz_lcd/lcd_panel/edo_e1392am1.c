void panel_pin_init(void)
{
#ifdef DEBUG
	int ret= 0;

	ret = gpio_request(edo_e1392am1_pdata.gpio_rest, "lcd mipi panel rest");
	if (!ret) {
		debug("request edo_e1392am1 lcd mipi panel rest gpio failed\n");
		return;
	}

	ret = gpio_request(edo_e1392am1_pdata.gpio_lcd_bl,
			"lcd mipi brightness power eanble");
	if (!ret) {
		debug("request edo_e1392am1 lcd brightness power eanble gpio failed\n");
		return;
	}
#endif

	debug("edo_e1392am1 panel display pin init\n");

	return;
}

void panel_power_on(void)
{
	gpio_direction_output(edo_e1392am1_pdata.gpio_rest, 1);
	mdelay(50);
	gpio_direction_output(edo_e1392am1_pdata.gpio_rest, 0);  //reset active low
	mdelay(50);
	gpio_direction_output(edo_e1392am1_pdata.gpio_rest, 1);
	udelay(600);
	debug("edo_e1392am1 panel power on\n");

	return;
}

